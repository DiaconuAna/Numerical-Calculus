function [A,B] = generate_matrix_2(n)
    A = zeros(n);
    B = zeros(n,1);
    B(1) = 3;
    B(2) = 2; B(3) = 2;
    B(n-2) = 2; B(n-1) = 2;
    B(n) = 3;
    A(1,1) = 5; A(1,2) = -1; A(1,4) = -1;
    A(n,n-3) = -1; A(n,n-1)=-1; A(n,n) = 5;
    A(2,1) = -1; A(2,2)=5; A(2,3) = -1; A(2,5) = -1;
    A(3,2) = -1; A(3,3) = 5; A(3,4) = -1; A(3,6) = -1;
    A(n-1, n-1) = 5; A(n-1,n) = -1; A(n-1,n-2) = -1; A(n-1,n-4) = -1;
    A(n-2, n-2) = 5; A(n-2, n-1) = -1; A(n-2, n-3) = -1; A(n-2, n-5) = -1;
    for j = 4:n-3
        B(j) = 1;
        A(j,j) = 5;
        A(j,j+1) = -1; A(j,j-1) = -1; A(j,j+3) = -1; A(j,j-3) = -1;
    end
end